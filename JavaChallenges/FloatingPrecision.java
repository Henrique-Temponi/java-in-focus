
/**
 * Convert a given number of pounds to kilograms
 */
public class FloatingPrecision {
    public static void main(String[] args) {
        
        double pounds = 195.5;
        double conversion = 0.45359237;
        double kilograms = pounds *conversion;

        System.out.println("The weight in kilograms: " + kilograms);
        
    }
}
